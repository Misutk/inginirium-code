from math import sqrt


class Triangle:
    def __init__(self, a, b, c):
        self.a = a
        self.b = b
        self.c = c

    def __init__(self):
        self.a = int(input())
        self.b = int(input())
        self.c = int(input())

    def per(self):
        return self.a + self.b + self.c

    def sqare(self):
        p = self.per() / 2
        s = sqrt(p*(p - self.a) * (p - self.b) * (p - self.c))
        return s
    def alpha(self):
        return math.asin(self.a/self.c)
    def __eq__(self, other):
        return self.a == other.a and self.b == other.b and self.c == other.c


